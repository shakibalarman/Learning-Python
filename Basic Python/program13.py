num = int(input("Enter a number: "))
if num > 0:
    print("This number is positive")
elif num < 0:
    print("This number is negative")
else:
    print("The number is zero")

# Checking even or odd for the same number
if num % 2 == 0:
    print("The number is even")
else:
    print("The number is odd")